void main() {
  String name = "Varun";
  // Use $ sign for String Interplation
  // $name, $a, $b it uses direct values
  
  print("My name is $name"); 
  print("the lenght of char in string is ${name.length}");

  int a = 20;
  int b = 30;

  print("The sum of $a and $b is ${a + b} ");
}