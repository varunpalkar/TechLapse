void main() {
  // for loop
  for (int i = 1; i <= 10; i++)
      if (i % 2 == 0) {
        print(i);
      }

// while loop
  var i = 1;
  while (i <= 10) {

    if (i % 2 == 0) {
    print(i);
  }
    i++;
}

// do-while loop
  int a = 1;
  do {
    if (a % 2 == 0) {
      print(a);
    }
    a++;
  } while (a <= 10);
}